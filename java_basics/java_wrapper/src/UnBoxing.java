

public class UnBoxing {

	// UnBoxing means Object to Primitive Casting
	public static void main(String[] args) {

		Double doubleObject = new Double(12);
		System.out.println("Double : " + doubleObject);

		double d = doubleObject;
		System.out.println("double : " + d);

		Byte bObj = 12;
		System.out.println("Byte : " + bObj);

		byte b = bObj;
		System.out.println("byte : " + b);

	}

}
